package com.delricco.vince.revolardemo.contacts;

public class RevolarContact {

    private String name;
    private String number;

    public RevolarContact(String name, String number) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public String getNumber() {
        return number;
    }
}
